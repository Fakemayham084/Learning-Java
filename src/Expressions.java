
public class Expressions {

	public static void main(String[] args) {
				
		int friends = 10;
		int cats = 3;
		int dogs = 4;
		double people = 100;
		int cookies = 18;
		
		friends = friends + 1; // Adding 1 to the friend INT for 11 friends
		cats = cats - 1; // Removing 1 from the cat INT for 2 cats
		dogs = dogs * 2; // Multiplying by 2 for the INT to have 8 dogs
		people = (double) people / 5; // Dividing by 5 for 20
		cookies = cookies % 2; // Gives the reminder of the number
		
		friends++; // will add 1 to the INT
		people--; // will remove 1
		
		
		
		//Print Statements
		System.out.println(friends);
		System.out.println(cats);
		System.out.println(dogs);
		System.out.println(people);
		System.out.println(cookies);
	}

}
